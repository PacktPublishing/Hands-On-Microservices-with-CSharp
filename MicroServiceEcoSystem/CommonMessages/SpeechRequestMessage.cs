using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace CommonMessages
{
    using EasyNetQ;

    /// <summary>   (Serializable) a speech request message. </summary>
    [Queue("Speech", ExchangeName = "EvolvedAI")]
    [Serializable]
    public class SpeechRequestMessage
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the identifier. </summary>
        ///
        /// <value> The identifier. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public int ID { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the text. </summary>
        ///
        /// <value> The text. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public string text { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the male speaker. </summary>
        ///
        /// <value> The male speaker. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public int maleSpeaker { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the volume. </summary>
        ///
        /// <value> The volume. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public int volume { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the rate. </summary>
        ///
        /// <value> The rate. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public int rate { get; set; }
    }
}
