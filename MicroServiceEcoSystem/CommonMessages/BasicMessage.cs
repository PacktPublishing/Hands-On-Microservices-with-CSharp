using System;

namespace CommonMessages
{
    using EasyNetQ;

    /// <summary>   (Serializable) a basic message. </summary>
    [Serializable]
    [Queue("General", ExchangeName = "EvolvedAI")]
    public class BasicMessage
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the text. </summary>
        ///
        /// <value> The text. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public string Text { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the random number. </summary>
        ///
        /// <value> The random number. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public int RandomNumber { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the Date/Time of the date. </summary>
        ///
        /// <value> The date. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public DateTime Date { get; set; }
    }
}
