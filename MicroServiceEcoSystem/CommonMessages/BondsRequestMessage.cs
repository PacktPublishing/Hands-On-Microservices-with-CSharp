using System;

namespace CommonMessages
{
    using EasyNetQ;

    /// <summary>   (Serializable) the bonds request message. </summary>
    [Queue("Financial", ExchangeName = "EvolvedAI")]
    [Serializable]
    public class BondsRequestMessage
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the Date/Time of the issue. </summary>
        ///
        /// <value> The issue. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public DateTime issue { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the Date/Time of the maturity. </summary>
        ///
        /// <value> The maturity. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public DateTime maturity { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the coupon. </summary>
        ///
        /// <value> The coupon. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public double coupon { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the frequency. </summary>
        ///
        /// <value> The frequency. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public int frequency { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the yield. </summary>
        ///
        /// <value> The yield. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public double yield { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets a message indicating if this is compounding or continuous. </summary>
        ///
        /// <value> The compounding. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public string compounding { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the price. </summary>
        ///
        /// <value> The price. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public double price { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the calculate yield. </summary>
        ///
        /// <value> The calculate yield. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public double calcYield { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the price 2. </summary>
        ///
        /// <value> The price 2. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public double price2 { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the message. </summary>
        ///
        /// <value> The message. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public string message { get; set; }
    }
}
