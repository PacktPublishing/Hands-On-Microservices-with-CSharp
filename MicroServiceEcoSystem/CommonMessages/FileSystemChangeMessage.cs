using System;

namespace CommonMessages
{
    using EasyNetQ;

    /// <summary>   (Serializable) a file system change message. </summary>
    [Serializable]
    [Queue("FileSystem", ExchangeName = "EvolvedAI")]
    public class FileSystemChangeMessage
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the identifier. </summary>
        ///
        /// <value> The identifier. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public long ID { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the type of the change. </summary>
        ///
        /// <value> The type of the change. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public int ChangeType { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the type of the event. </summary>
        ///
        /// <value> The type of the event. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public int EventType { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the change date. </summary>
        ///
        /// <value> The change date. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public DateTime ChangeDate { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the full pathname of the file. </summary>
        ///
        /// <value> The full pathname of the file. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public string FullPath { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the full pathname of the old file. </summary>
        ///
        /// <value> The full pathname of the old file. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public string OldPath { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the name of the file or directory. </summary>
        ///
        /// <value> The name. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public string Name { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the old name of the file or directory. </summary>
        ///
        /// <value> The name of the old. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public string OldName { get; set; }
    }
}
