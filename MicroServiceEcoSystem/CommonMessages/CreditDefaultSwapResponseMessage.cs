using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace CommonMessages
{
    using EasyNetQ;

    /// <summary>   (Serializable) a credit default swap message. </summary>
    [Queue("Financial", ExchangeName = "EvolvedAI")]
    [Serializable]
    public class CreditDefaultSwapResponseMessage
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the identifier. </summary>
        ///
        /// <value> The identifier. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public long ID { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the fixed rate. </summary>
        ///
        /// <value> The fixed rate. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public double fixedRate { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the notional. </summary>
        ///
        /// <value> The notional. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public double notional { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the recovery rate. </summary>
        ///
        /// <value> The recovery rate. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public double recoveryRate { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the fair rate. </summary>
        ///
        /// <value> The fair rate. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public double fairRate { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the fair npv. </summary>
        ///
        /// <value> The fair npv. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public double fairNPV { get; set; }
    }
}
