using System;

namespace CommonMessages
{
    using EasyNetQ;

    /// <summary>   Values that represent Milliseconds status. </summary>
    public enum MSStatus
    {
        /// <summary>   An enum constant representing the healthy= 1 option. </summary>
        Healthy=1,

        /// <summary>   An enum constant representing the unhealthy= 2 option. </summary>
        Unhealthy=2
    }

    /// <summary>   (Serializable) a health status message. </summary>
    [Serializable]
    [Queue("Health", ExchangeName = "EvolvedAI")]
    public class HealthStatusMessage
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the identifier. </summary>
        ///
        /// <value> The identifier. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public string ID { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the Date/Time of the date. </summary>
        ///
        /// <value> The date. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public DateTime date { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the name of the service. </summary>
        ///
        /// <value> The name of the service. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public string serviceName { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the status. </summary>
        ///
        /// <value> The status. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public int status { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the message. </summary>
        ///
        /// <value> The message. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public string message { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the memory used. </summary>
        ///
        /// <value> The memory used. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public double memoryUsed { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the CPU. </summary>
        ///
        /// <value> The CPU. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public double CPU { get; set; }
    }
}
